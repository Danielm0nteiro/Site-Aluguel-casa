let map;
let polyline;
let markerStart;
let markerEnd;

function initMap() {
    map = L.map('map').setView(route1[0], 16);
    const layer = L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors',
        maxZoom: 15
    });
    layer.addTo(map);

    markerStart = L.marker(route1[0]);
    markerStart.addTo(map);

    markerEnd = L.marker(route1[route1.length - 1]);
    markerEnd.addTo(map);

    polyline = L.polyline(route1, { color: '#F00' });
    polyline.addTo(map);
}

function loadRoute(route) {
    if (polyline) {
        map.removeLayer(polyline);
        map.removeLayer(markerStart);
        map.removeLayer(markerEnd);
    }
    polyline = L.polyline(route, { color: '#F00' });
    polyline.addTo(map);

    markerStart = L.marker(route[0]);
    markerStart.addTo(map);

    markerEnd = L.marker(route[route.length - 1]);
    markerEnd.addTo(map);

    map.setView(route[0], 16);
}

function loadRoute1() {
    loadRoute(route1);
}

function loadRoute2() {
    loadRoute(route2);
}

document.addEventListener('DOMContentLoaded', initMap);